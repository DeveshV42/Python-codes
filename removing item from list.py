#removing item from list
l1=[1,2,3,4,5,6,7]
print("current list:",l1)

l1.remove(2)
print("updated list",l1)

#pop method for removing last value from list
l2=[1,2,3,4,5,6,7,8,9]
print("current list:",l1)

l2.pop()
print("print updated list:",l2)

#del method removing item from index
del l2[3]
print("updated list:",l2)

#clear method used for clear the list
l3=[1,2,3,4,5,6,7]
print("current list",l3)

l3.clear()
print("updated list:",l3)



